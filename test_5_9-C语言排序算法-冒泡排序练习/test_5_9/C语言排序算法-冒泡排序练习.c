#define  _CRT_SECURE_NO_WARNINGS
#include<stdio.h> // 包含标准输入输出库，以便使用printf等函数

void main() // 主函数入口
{
    int n[10] = { 25,35,68,79,21,13,98,7,16,62 }; // 定义并初始化一个包含10个整数的数组

    int i, j, k, temp; // 定义循环变量i、j、k和交换临时变量temp

    for (i = 1; i <= 9; i++) // 外层循环，控制排序的轮数，总共需要比较9轮（因为数组长度为10，最后一轮只剩一个元素无需比较）
    {
        for (j = 0; j <= 9 - i; j++) // 内层循环，每轮开始时比较数组中的相邻元素，随着外层循环推进，已排序的最大元素会被推到最后，因此内层循环范围逐渐减小
        {
            if (n[j] > n[j + 1]) // 检查当前元素是否大于下一个元素，如果是则交换它们
            {
                temp = n[j]; // 临时存储n[j]的值
                n[j] = n[j + 1]; // 将n[j + 1]的值赋给n[j]
                n[j + 1] = temp; // 将之前存储的n[j]的值赋给n[j + 1]，完成交换
            }
        }
        printf("第%d趟排序完成后的数据排序:\n", i); // 打印当前轮次信息
        for (k = 0; k < 10; k++) // 打印当前排序状态的数组
            printf("%-4d", n[k]); // 使用%-4d格式化输出，保证每个数字间有固定空格，左对齐
        printf("\n"); // 换行
    }

    printf("排序过后的数顺序:\n"); // 打印最终排序完成的提示信息
    for (i = 0; i < 10; i++) // 遍历并打印最终排序好的数组
        printf("%-4d", n[i]); // 同样使用%-4d格式化输出
    printf("\n"); // 结束时换行
}